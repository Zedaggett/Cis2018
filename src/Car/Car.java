package Car;
//Describing variables of the object Car
//The following code describes attributes of Car
public class Car {
	private String color;
	private String make;
	private String model;
	private String year;
	
	public String getColor() {
		return color;
	}
	public void setColor (String color) {
		this.color= color;
	}
	public String getMake() {
		return make;
	}
	public void setMake (String make) {
		this.make = make;
	}
	public String getModel() {
		return model;
}
	public void setModel (String model) {
		this.model = model;
	}
	public String getYear() {
		return year;
	}
	public void setYear (String year) {
		this.year = year;
	}
	
}